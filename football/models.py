class Team(models.Model):
    id = models.BigAutoField(primary_key=True)
    name = models.TextField()
    points = models.BigIntegerField(blank=True, null=True)

    def __str__(self):
        return f"{self.id}, {self.name}, {self.points}"

    class Meta:
        db_table = 'teams'


